public class WrapperClass {
    
    public static void main(String[] args) {
        Integer myInt = 7;
        Double myDouble = 5.55;
        Character myChar = 'A';
        System.out.println(myInt);
        System.out.println(myInt.intValue());

        System.out.println(myDouble);
        System.out.println(myChar);

    }
}
